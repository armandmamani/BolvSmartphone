function calculateTotal() {
  const apartmentID = document.getElementById("apartment_select").value;
  const apt = apartmentDetails.find(a => a.id === apartmentID);

  if (!apt) {
    alert("Please select a valid apartment.");
    return;
  }

  const priceNet = parseFloat(document.getElementById("price_net").value) || 0;
  const priceCommon =
    parseFloat(document.getElementById("price_common").value) || 0;
  const priceTerrace =
    parseFloat(document.getElementById("price_terrace").value) || 0;
  const pricePlot =
    parseFloat(document.getElementById("price_plot").value) || 0;
  const priceParking =
    parseFloat(document.getElementById("price_parking").value) || 0;
  const pricePool =
    parseFloat(document.getElementById("price_pool").value) || 0;
  const priceDepo =
    parseFloat(document.getElementById("price_depo").value) || 0;

  const items = [
    { label: "Sipërfaqe Neto", qty: apt.NetArea, unit: priceNet },
    { label: "Sipër.e përbashkët", qty: apt.ComonArea, unit: priceCommon },
    { label: "Veranda", qty: apt.TerraceArea, unit: priceTerrace },
    { label: "Oborri", qty: apt.GreenArea, unit: pricePlot },
    { label: "Parkim", qty: 2, unit: priceParking }
  ];

  const tbody = document.getElementById("result_body");
  tbody.innerHTML = "";

  let total = 0;
  items.forEach(item => {
    const subtotal = item.qty * item.unit;
    total += subtotal;

    const row = document.createElement("tr");
    row.innerHTML = `
      <td style="padding: 5px; border: 1px solid #ccc;">${item.label}</td>
      <td style="text-align: right; padding: 5px; border: 1px solid #ccc;">${item.qty.toFixed(
        2
      )}</td>
      <td style="text-align: right; padding: 5px; border: 1px solid #ccc;">€${item.unit.toLocaleString(
        undefined,
        { minimumFractionDigits: 0 }
      )}</td>
      <td style="text-align: right; padding: 5px; border: 1px solid #ccc;">€${subtotal.toLocaleString(
        undefined,
        { minimumFractionDigits: 0 }
      )}</td>
    `;
    tbody.appendChild(row);
  });

  document.getElementById("total_cell").textContent = `€${total.toLocaleString(
    undefined,
    { minimumFractionDigits: 0 }
  )}`;
  document.getElementById("result_title").textContent = `Apartamenti ${apt.id}`;
  document.getElementById("detailed_result").style.display = "block";
}
